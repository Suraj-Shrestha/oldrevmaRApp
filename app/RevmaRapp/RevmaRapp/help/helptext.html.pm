◊h2{Aktivitetsoversikt}

Ved hjelp av den grafiske aktivitetsoversikten kan du få oversikt over alle aktiviteter som er registrert i en eller flere perioder. 

◊h3{Velge perioder}

Velg den eller de periodene du ønsker å studere.  De valgte periodene blir markert med en hake til høyre. 

◊h3{Grafisk overskit}

Grafen viser alle aktiviteter i den eller de periodene du har valgt. Hver aktivitet vises som et aktivitetssymbol. De aktivitetene som vises øverst til høyre er de aktivitetene som er viktigst og mest lystbetont, mens aktiviteter nederst til venstre er de aktivitetene som er mist viktige og som først og fremst oppleves som plikt. 

Ved å trykke på et av de fire hoved områdene i grafen, øverst eller nederst til høyre eller venstre, kan du få listet opp alle aktivitetene i dette området.

◊h3{Liste over aktiviteter}

Ved å kutte ned på noen røde aktiviteter, og heller gjøre grønne aktiviteter som gir energi, kan du komme inn i en bedre sirkel som på sikt kan gi deg mer overskudd.  Dette kan føre til bedre mulighet til å delta i aktiviteter som er viktige for deg. 
