◊h1{◊|rrap| Hjelptekst}

◊h2{Hva er ◊|rrap|?}

◊|rrap| er utviklet for deg som sliter med utmattelse. Den er et verktøy for å få bedre oversikt over hvordan daglige aktiviteter påvirker energi og mestring.  Ved hjelp av denne appen kan du registrere daglige aktiviteter og vurdere aktivitetene i forhold til energibruk, plikt, viktighet og mestring.

Formålet med dette er å bidra til bedre oversikt over dine aktivitetsmønstre, energi, mestring og trivsel i hverdagen. Dette kan gi grunnlag for refleksjon rundt hvordan oppnå best mulig balanse mellom ulike aktiviteter i forhold til egne forutsetninger og livssituasjon.

◊h2{Perioder}

Det anbefales at du gjør en detaljert registrering av alle aktiviteter i minst 3 dager og inntil en uke. Velg gjerne en periode som inneholder en dag i en helg. Du kan gi registreringsperioden et navn som gir mening for deg.  

Du kan gjenta den detaljerte registreringen på et senere tidspunkt, for eksempel om 2–3 måneder, for å se om det har skjedd en endring i positiv retning. Da kan du bruke funksjonen for å sammenligne perioder.

◊h2{Aktivitetsperioder}

Her får du oversikt over alle registrerte perioder. Du kan begynne å registrere en ny periode ved å trykke på pluss i høyre hjørne. 

◊h2{Registrere aktivitet}

Når du skal registrere aktiviteter kan du velge fra den forhåndsutfylte listen med aktiviteter. Du kan gjerne endre navn eller legge til nye aktiviteter slik at det passer din hverdag.

Det anbefales å dele aktiviteter helt ned i times eller halvtimes intervaller.  Poenget med dette er å få innsikt i hvordan du bruker kreftene og hvor viktige de ulike aktivitetene er, og i hvilken grad aktivitetene er lystbetonte. Ved å gjennomføre en så detaljert registrering vil du kunne bli mer oppmerksom på variasjoner gjennom dagen. 

Det kan være slitsomt å registrere såpass detaljert, og derfor anbefales en relativt kort registreringsperiode på mellom 3 dager og inntil en uke.  

◊h2{Vurdering av aktivitet}

For hver aktivitet angis tidspunkt og varighet. Ved hjelp av glideskalaen angis i hvilken grad aktiviteten er viktig for deg, om du kun følte plikt eller om den var mest lystbetont, og hvor mye energi du brukte på aktiviteten.  

Legg merke til aktivitetssymbolet som endrer farge og størrelse på grunnlag av tidsbruk og hvordan du vurderer aktiviteten. Jo mer tid du brukte på aktiviteten, jo større blir symbolet. Hvis du brukte mye energi på aktiviteten blir symbolet rødt, mens hvis du fikk energi blir symbolet grønt. 

De grønne aktivitetene er aktiviteter som du angir at er viktige for deg og som du opplever som lystbetonte oppgaver. Jo mørkere grønnfargen er, jo mer viktig og lystbetonte er aktiviteten.

De røde aktiviteter er aktiviteter som du angir at er uviktige for deg og som du opplever mest som pliktoppgaver. Jo mørkere rødfargen er, jo mindre viktig og mer pliktbetont er aktiviteten.  I aktivitetsoversikten vises alle aktivitetene ved hjelp av aktivitetssymbolet. 

